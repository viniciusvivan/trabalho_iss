    public boolean validaCampos() {
        boolean ret = true;
        if (txtnome.getText().equals("") || txtnome.getText() == null){
            JOptionPane.showMessageDialog(null,"Campo Nome obrigatório !","Alerta",JOptionPane.INFORMATION_MESSAGE);
            txtnome.requestFocus();
            ret=false;
        }else if(txtUni.getText() == null || txtUni.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Campo Unidade de Medida obrigatório !","Alerta",JOptionPane.INFORMATION_MESSAGE);
            txtUni.requestFocus();
            ret=false;
        }
        else if(txtQnt.getText() == null || txtQnt.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Campo Quantidade em Estoque obrigatório !","Alerta",JOptionPane.INFORMATION_MESSAGE);
            txtQnt.requestFocus();
            ret=false;
        }else if(txtPreco.getText() == null || txtPreco.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Campo Preço obrigatório !","Alerta",JOptionPane.INFORMATION_MESSAGE);
            txtPreco.requestFocus();
            ret=false;
       }
        return ret;
    }
    

