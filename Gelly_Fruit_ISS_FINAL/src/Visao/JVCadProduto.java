    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //ALTERAR
        ArrayList<String> Registro = new ArrayList<String>();
        
        if(validaCampos() == true){
            Registro.add(txtcodigo.getText());
            Registro.add(txtnome.getText());
            Registro.add(txtUni.getText());
            Registro.add(txtQnt.getText());
            Registro.add(txtPreco.getText());
            Registro.add(String.valueOf(cbmStatus.getSelectedIndex()));
            
            CProduto cproduto = new CProduto();
            cproduto.Alterar(Registro);

            JOptionPane.showMessageDialog(null,"Produto alterado com sucesso !","Alterar",JOptionPane.INFORMATION_MESSAGE);
            LimpaCampos();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        //SALVAR
        ArrayList<String> Registro = new ArrayList<String>();
        
        if(validaCampos() == true){
            
            Registro.add(txtnome.getText());
            Registro.add(txtUni.getText());
            Registro.add(txtQnt.getText());
            Registro.add(txtPreco.getText());
            Registro.add(String.valueOf(cbmStatus.getSelectedIndex()));

            CProduto cproduto = new CProduto();
            cproduto.Salvar(Registro);

            JOptionPane.showMessageDialog(null,"Produto salvo com sucesso !","Salvar",JOptionPane.INFORMATION_MESSAGE);
            LimpaCampos();

        }
    }//GEN-LAST:event_jButton3ActionPerformed

