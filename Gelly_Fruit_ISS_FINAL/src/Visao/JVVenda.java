    public boolean validaCampos() {
        boolean ret = true;
        if (txtcodigo.getText().equals("") || txtcodigo.getText() == null){
            JOptionPane.showMessageDialog(null,"Campo codigo obrigatório !","Alerta",JOptionPane.INFORMATION_MESSAGE);
            txtcodigo.requestFocus();
            ret=false;
        }     
        return ret;
    }

