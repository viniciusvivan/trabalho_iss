    public void PreencheCampos(int codigo) {
        ArrayList<Vendas> vend = new ArrayList<Vendas>();   
        vend = CVenda.BuscaCadastro(codigo);
        
        Vendas objVendasBuffer;     
        objVendasBuffer = vend.get(0);
        
        txtCodVenda.setText(String.valueOf(objVendasBuffer.getId_Venda()));
        //txtnome.setText(objVendasBuffer.getsatus_Venda());
        txtData.setText(objVendasBuffer.getdata_Venda());
        txtcodigo.setText(objVendasBuffer.getcod_Cliente());
        txtCodProduto.setText(objVendasBuffer.getcod_Produto());
        txtValorProduto.setText(objVendasBuffer.getvalorprod_Venda());
        //jComboBoxDist.setText(objVendasBuffer.getentrega_Venda());
        txtValorTotal.setText(objVendasBuffer.getvalortotal_Venda());
        //txt.setText(objVendasBuffer.getpagamento_Venda());
        txtPreco.setText(objVendasBuffer.getquantidade_Venda());
   }
    
     
    
    
    
