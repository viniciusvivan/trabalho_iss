    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        // BUSCA
        ArrayList<String> Parametros = new ArrayList<String>();
        Parametros.add(String.valueOf(jcbxTipo.getSelectedIndex()));
        Parametros.add(txtCampo.getText().toUpperCase());                     // LETRAS MAIUSCULAS P/ MINUSCULAS

        //estrutura da tabela
        Vector<String> vColunas = new Vector<String>();
        vColunas = pegaColunasDaGrade();

        //modelo da tabela
        DefaultTableModel objTabela = new DefaultTableModel(vColunas,0);//passando quais sãoa as colunas e qntas linhas são

        //criando o controller da aplicação para efetuar a chamada da consulta
        CVenda CntrlVenda = new CVenda();
        objTabela = CntrlVenda.PesquisaObjeto(Parametros,objTabela);

        tabelaVenda.setModel(objTabela);
    }//GEN-LAST:event_jButton1ActionPerformed

