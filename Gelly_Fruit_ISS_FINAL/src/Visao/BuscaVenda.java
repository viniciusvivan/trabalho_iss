    private void btmConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmConfirmarActionPerformed

        int linha = tabelaVenda.getSelectedRow();

        if(linha == -1){
            JOptionPane.showMessageDialog(rootPane,"Selecione o Resgisto Desejado !","Erro ao Consultar Cadastro",JOptionPane.INFORMATION_MESSAGE);
        } else{
            int Codigo = Integer.valueOf(tabelaVenda.getValueAt(linha, 0).toString());

            cadvend.PreencheCampos(Codigo);
            this.setVisible(false);
            this.dispose();// distribui a instancia do objeto Codigo

        }
    }//GEN-LAST:event_btmConfirmarActionPerformed

